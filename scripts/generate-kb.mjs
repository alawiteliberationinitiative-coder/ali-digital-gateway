const questions = [];
let id = 1;

function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function addSort(cat, sentence, explanation) {
  const words = sentence.split(" ");
  questions.push({
    id: id++, category: cat, type: "sort",
    text: "رتّب الكلمات لتكوين الحكمة الصحيحة:",
    words: shuffle(words), answer: words, explanation
  });
}

function addChoice(cat, text, options, answer, explanation) {
  questions.push({
    id: id++, category: cat, type: "choice",
    text, options, answer, explanation
  });
}

