const questions = [];
let id = 1;

function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function addSort(cat, sentence, source) {
  const words = sentence.split(" ");
  questions.push({ id: id++, type: "sort", category: cat,
    instruction: "رتّب الكلمات لتكوين الجملة الصحيحة",
    words: shuffle(words), answer: words, source });
}

function addFill(cat, text, answer, distractors, source) {
  const opts = shuffle([answer, ...distractors.slice(0, 3)]);
  questions.push({ id: id++, type: "fill", category: cat,
    instruction: "اختر الكلمة المناسبة لملء الفراغ",
    text, options: opts, answer, source });
}

function addChoice(cat, text, options, answer, source) {
  questions.push({ id: id++, type: "choice", category: cat,
    instruction: "اختر الإجابة الصحيحة",
    text, options, answer, source });
}

