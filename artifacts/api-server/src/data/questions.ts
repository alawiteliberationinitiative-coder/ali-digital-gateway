// ─── Helper functions ────────────────────────────────────────────────────────

export function getQuestionsByDifficulty(d: number): Question[] {
  return QUESTION_BANK.filter(q => q.difficulty === d);
}

export function getQuestionById(id: number): Question | undefined {
  return QUESTION_BANK.find(q => q.id === id);
}

export function getAllQuestions(): Question[] {
  return QUESTION_BANK;
}

export function generatePoolForStage(
  stage: number,
  count: number,
  excludeIds: number[] = []
): number[] {
  const difficulty = Math.min(Math.max(stage, 1), 5);
  const candidates = QUESTION_BANK.filter(
    q => q.difficulty === difficulty && !excludeIds.includes(q.id)
  );
  if (candidates.length === 0) {
    // Fall back to any questions not yet answered
    const fallback = QUESTION_BANK.filter(q => !excludeIds.includes(q.id));
    const shuffled = fallback.sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count).map(q => q.id);
  }
  const shuffled = candidates.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map(q => q.id);
}
